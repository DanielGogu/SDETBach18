package ReplitTasks;
/***For you to do:**

 Using while loop print numbers from 15 to 0
 **Example Output:**
15-0 */
public class T47While15To0 {
    public static void main(String[] args) {
        int i=15;
        while (i>=0){
            System.out.println(i);
            i--;
        }
    }
}
