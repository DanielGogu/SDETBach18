package Class2ConcatRO;

public class E18RelationalOP {
    public static void main(String[] args) {
        int num1=10;
        int num2=20;
        System.out.println(num1>num2); //False
        System.out.println(num1<num2); //True
        System.out.println(num1==num2); //False //To ask if is equal you need "=="
        System.out.println(num1!=num2); // True //To ask if is not equal you need "!="






    }
}
