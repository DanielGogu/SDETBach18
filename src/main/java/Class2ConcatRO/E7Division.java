package Class2ConcatRO;

public class E7Division {
    public static void main(String[] args) {
        // no decimal results after the coma because it's an "int" container
        int num1=10;
        int num2=3;


     // % = it gives us the remainder part
        System.out.println(num1%num2);


        // insead we should use "double" container
        double num3=10;
        double num4=3;
        System.out.println(num3/num4);







    }
}
