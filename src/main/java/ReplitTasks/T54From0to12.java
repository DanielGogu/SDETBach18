package ReplitTasks;
/*
Create a for loop that prints out numbers from 0 to 12

Must not include the number 12 in the output */
public class T54From0to12 {
    public static void main(String[] args) {
        int i=0;
        while (i<12){
            System.out.println(i);
            i++;
        }
    }
}
