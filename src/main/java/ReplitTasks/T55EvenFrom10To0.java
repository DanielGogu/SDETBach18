package ReplitTasks;
/*
Create a for loop that prints out even numbers from 10 to 0

Must not include the number 0 in the output */
public class T55EvenFrom10To0 {
    public static void main(String[] args) {
        int i=10;
        while (i>=0){
            System.out.println(i);
            i=i-2;
        }

    }
}
